void abc(uint8_t i, uint16_t x)
{
  s[i]++;
  s[i]=s[i]%2;
  if(s[i]==0)
  {
    n[i]=x;
    if(n[i]>u[i])
    {
      pwm_vals[i]= n[i]-u[i];
    }
    else
    {
      pwm_vals[i]=(10000-u[i])+n[i];
    }
  }
  else
  {
    u[i]=x;
  }
}




