void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
  uint8_t i;
  if(uart_incoming == '['){
    for(i=0; i<8; i++){
      msg[i] = 0;
    }
    msg[0] = '[';
  }
  else if(uart_incoming == ']')
  {
    msg[7] = ']';
    if(!(msg[0]=='[' && msg[7]==']')) return;
    uint8_t temp_str[4];
    for(i=0;i<3;i++){
      temp_str[i] = msg[i+1];
    }
    temp_str[3] = 0;
    adc_vals[0] = strtol(temp_str, NULL, 16);
    for(i=0;i<3;i++){
      temp_str[i] = msg[i+4];
    }
    temp_str[3] = 0;
    adc_vals[1] = strtol(temp_str, NULL, 16);
    TIM16 -> CCR1 = adc_vals[0];
    TIM17 -> CCR2 = adc_vals[1];
    HAL_UART_Transmit_IT(&huart2, msg, 8);
    HAL_HalfDuplex_EnableTransmitter(&huart1);
    uart_mode=1;
  }
  else {
    for(i=7; i>=0; i--){
      if(msg[i] == 0) continue;
      else break;
    }
    i++;
    msg[i] = uart_incoming;
  }
  if(uart_incoming != ']'){
    HAL_UART_Receive_IT(&huart1, &uart_incoming, 1);
  }
}

